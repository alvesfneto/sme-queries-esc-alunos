
--LISTA DE UNIDADES CCA COM TURMAS E MATRÍCULAS

SELECT 
DRE,
CODESC,
TIPOESC,
NOMESC,
ENDERECO,
NUMERO,
BAIRRO,
TEL1,
TEL2,
FAX,
EMAIL,
ISNULL(COUNT(CD_TURMA_ESCOLA),0) AS QT_TURMAS,
ISNULL(SUM(B.AL_AT),0) AS MATRIC,
[DATABASE] 
INTO ##escolas_lista_ccas_turmas_matriculas
FROM VW_CADASTRO_ESCOLAS_DIVULGACAO_EOL A
LEFT JOIN SME_CLASSES_DIARIO B ON A.CODESC=B.CD_ESCOLA

WHERE TIPOESC='CCA'
GROUP BY
DRE,
CODESC,
TIPOESC,
NOMESC,
ENDERECO,
NUMERO,
BAIRRO,
TEL1,
TEL2,
FAX,
EMAIL,
[DATABASE]

SELECT * FROM SME_CLASSES_DIARIO