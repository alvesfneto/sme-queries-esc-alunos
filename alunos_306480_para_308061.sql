*/

SELECT
CONCAT (cd_escola,' ',SG_TP_ESCOLA,' ',NOME_ESCOLA) AS ESCOLA_ORIGEM, 
NOME_ALUNO AS 'NOME DO ALUNO',
CONVERT(INT,COD_ALUNO)  AS CD_ALUNO,
ETAPA,
SERIE_ANO,
CONCAT (UPPER (TP_LOGRADOURO),' ',ENDEREÇO) AS ENDEREÇO,
NR AS NÚMERO,
COMPL AS COMPLEMENTO,
BAIRRO,
DIST_ESCOLA,
'CEI CLUBE DAS LETRINHAS' AS NOVO_DESTINO,
FLOOR(FLOOR(SQRT(POWER((LAT_ALUNO * 1000000 - ( -23.579822 * 1000000) ),2) +  POWER((LON_ALUNO * 1000000  - (-46.548837 * 1000000)),2)) / 10)/0.6) AS DISTANCIA

--TEG AS 'USO DO TEG'
INTO ##alunos_306480_PARA_308061
FROM VW_ENDERECOS_ALUNOS_COM_RESPONSAVEIS
WHERE CD_ESCOLA= 306480

